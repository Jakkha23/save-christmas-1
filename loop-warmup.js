// "While" loop
let temperature = 20

while(temperature < 25) {
    console.log(temperature + '°')
    temperature++
}

console.log(`the water is boiling, the temperature is now ${temperature}° degrees!`)

// "For" loop

i = 1

for (i = 1; i < 20; ++i) {
    console.log(i)
}

console.log(`'i' is now ${i}`)